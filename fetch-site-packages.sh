#!/bin/sh

cd -- "$(dirname "$0")" || exit 1
mkdir -v -p -- "site-packages" || exit 1
cd -- "site-packages" || exit 1

pypi_package="bottle-0.11.6"
echo "#################### BEGIN: $pypi_package ####################"
wget --output-document="$pypi_package.tar.gz" -- \
        "http://pypi.python.org/packages/source/b/bottle/$pypi_package.tar.gz" || exit 1
tar -v -xa --file="$pypi_package.tar.gz" || exit 1
rm -v -f -- "$pypi_package.tar.gz" || exit 1
ln -v -sf -- "$pypi_package/bottle.py" || exit 1
echo "#################### DONE: $pypi_package ####################"

pypi_package="Mako-0.7.3"
echo "#################### BEGIN: $pypi_package ####################"
wget --output-document="$pypi_package.tar.gz" -- \
        "http://pypi.python.org/packages/source/M/Mako/$pypi_package.tar.gz" || exit 1
tar -v -xa --file="$pypi_package.tar.gz" || exit 1
rm -v -f -- "$pypi_package.tar.gz" || exit 1
ln -v -sf -- "$pypi_package/mako" || exit 1
echo "#################### DONE: $pypi_package ####################"

true
